    private void buyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buyButtonActionPerformed
        try {
    // 1. Get what the user selected and typed
    int selectedIndex = productDropdown.getSelectedIndex(); // 0 for Rice, 1 for Sugar, etc.
    String inputText = qtyInput.getText();
    int qty = Integer.parseInt(inputText); // Converts the typed text into an integer
    
    // 2. Error Handling: Prevent negative or zero quantities
    if (qty <= 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid quantity greater than 0.");
        return; // Stops the code from continuing
    }
    
    // 3. Error Handling: Prevent buying more than what is in stock
    if (qty > StoreDatabase.stockAvailable[selectedIndex]) {
        javax.swing.JOptionPane.showMessageDialog(this, "Insufficient stock! Only " 
                + StoreDatabase.stockAvailable[selectedIndex] + " left.");
        return; 
    }
    
    // 4. Computations: Calculate the total cost
    double totalCost = qty * StoreDatabase.priceList[selectedIndex];
    
    // 5. Array Updates: Deduct from inventory
    StoreDatabase.stockAvailable[selectedIndex] -= qty;
    
    // 6. Array Updates: Save to transaction history logs
    // We use the 'transactionCount' as the index so we know exactly which slot to fill in the array
    StoreDatabase.historyItems[StoreDatabase.transactionCount] = StoreDatabase.productList[selectedIndex];
    StoreDatabase.historyQuantities[StoreDatabase.transactionCount] = qty;
    StoreDatabase.historyTotals[StoreDatabase.transactionCount] = totalCost;
    
    // Move the counter up by 1 so the next purchase goes into the next slot
    StoreDatabase.transactionCount++; 
    
    // 7. Success Message
    javax.swing.JOptionPane.showMessageDialog(this, "Purchase Successful!\nTotal Cost: â‚±" + totalCost);
    
    // 8. Clear the text field for the next customer
    qtyInput.setText("");
    
} catch (NumberFormatException e) {
    // Error Handling: If the user types letters like "ABC" instead of a number
    javax.swing.JOptionPane.showMessageDialog(this, "Error: Quantity must be a whole number.");
}
    }//GEN-LAST:event_buyButtonActionPerformed

